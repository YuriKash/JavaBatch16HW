package HWClass9;

public class HW8 {
    public static void main(String[] args) {
        /*Create an array to store char values and then print those in reverse order
         */

        char[] new1={'a','b','c','d'};
        for (int i = new1.length-1; i >=0 ; i--) {
            System.out.println(new1[i]);

        }
    }
}
